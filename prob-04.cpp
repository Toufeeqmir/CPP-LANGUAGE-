
#include <iostream>

using namespace std;

int main()
{
    int a = 3;
    int b = 4;
    cout << "following are the Arthimetic operators" << endl;
    cout << "the value of a + b is " << (a + b) << endl;
    cout << "the value of a%b is " << a % b << endl;
    cout << "the value of a++ is " << a++ << endl;
    cout << "the value of a-- is " << a-- << endl;
    cout << "the value of --a is " << --a << endl;
    cout << "the value of ++a is " << ++a << endl;
}